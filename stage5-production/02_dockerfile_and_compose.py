def explain_volume_strategy():
    """
    卷挂载策略
    """
    print("=" * 60)
    print("卷挂载策略")
    print("=" * 60)
    print()
    print("三种挂载方式，各有用途：")
    print()
    print("1. Bind Mount（绑定挂载）— 宿主机目录映射")
    print("   用途：模型缓存、配置文件")
    print()
    print(f"   -v {HF_HOME}:/root/.cache/huggingface:ro")
    print("        ↑ 宿主机路径              ↑ 容器路径      ↑ 只读")
    print()
    print("   为什么要 :ro（只读）？")
    print("   - 防止容器意外修改宿主机的模型缓存")
    print("   - 多个容器共享同一缓存时，避免并发写入冲突")
    print("   - 如果需要下载模型，在宿主机上操作，不在容器内")
    print()
    print("   -v ./nginx/nginx.conf:/etc/nginx/nginx.conf:ro")
    print("   -v ./prometheus/prometheus.yml:/etc/prometheus/prometheus.yml:ro")
    print("   配置文件也用 :ro，容器不应修改配置")
    print()
    print("2. Named Volume（命名卷）— Docker 管理的持久化存储")
    print("   用途：Prometheus 数据、Grafana 数据")
    print()
    print("   volumes:")
    print("     prometheus-data:      # 声明命名卷")
    print("     grafana-data:")
    print()
    print("   服务中使用：")
    print("   volumes:")
    print("     - prometheus-data:/prometheus    # 监控数据持久化")
    print("     - grafana-data:/var/lib/grafana  # 仪表盘配置持久化")
    print()
    print("   优势：")
    print("   - 容器删除后数据不丢失")
    print("   - Docker 自动管理存储位置")
    print("   - 不关心宿主机上的具体路径")
    print()
    print("3. tmpfs — 内存文件系统（临时，不常用）")
    print("   适合存放临时数据，容器停止即消失")
    print()


