def explain_dockerfile_structure():
    """
    Dockerfile 指令详解
    """
    print("=" * 60)
    print("Dockerfile 指令详解")
    print("=" * 60)
    print()
    print("vLLM 自定义 Dockerfile 示例（见 docker/Dockerfile.vllm）：")
    print()
    print("```dockerfile")
    print("# 1. FROM — 基础镜像")
    print("FROM vllm/vllm-openai:v0.19.0")
    print("#  → 所有后续指令都基于此镜像构建")
    print("#  → 已包含 Python, vLLM, CUDA, 系统库")
    print()
    print("# 2. ENV — 设置环境变量")
    print('ENV HF_HOME=/root/.cache/huggingface \\')
    print('    PYTORCH_CUDA_ALLOC_CONF=expandable_segments:Max:640x64000')
    print("#  → 容器运行时这些变量可用")
    print("#  → PYTORCH_CUDA_ALLOC_CONF 减少显存碎片")
    print()
    print("# 3. HEALTHCHECK — 健康检查")
    print("HEALTHCHECK --interval=30s --timeout=10s --start-period=120s --retries=3 \\")
    print("    CMD curl -f http://localhost:8000/health || exit 1")
    print("#  → 每 30s 检查一次 /health 端点")
    print("#  → start-period=120s：模型加载需要 30-90s，给足启动时间")
    print("#  → 连续 3 次失败才标记为 unhealthy")
    print()
    print("# 4. EXPOSE — 声明端口（文档作用）")
    print("EXPOSE 8000")
    print("#  → 不实际发布端口，仅作为文档说明")
    print("#  → 实际端口映射在 docker run -p 或 compose 中配置")
    print()
    print("# 5. ENTRYPOINT — 默认启动命令")
    print('ENTRYPOINT ["python", "-m", "vllm.entrypoints.openai.api_server"]')
    print("#  → 容器启动时执行的命令")
    print("#  → 可被 docker-compose command 覆盖")
    print("```")
    print()


def explain_healthcheck():
    """
    HEALTHCHECK 对 LLM 服务的意义
    """
    print("=" * 60)
    print("HEALTHCHECK：LLM 服务的健康检查")
    print("=" * 60)
    print()
    print("为什么 HEALTHCHECK 对 LLM 部署特别重要？")
    print()
    print("  vLLM 启动流程：")
    print("  [启动进程] → [加载模型权重 30-90s] → [编译 CUDA Graph 10-30s] → [就绪]")
    print()
    print("  Docker 容器启动 ≠ 服务可用！")
    print("  容器状态 running，但 vLLM 可能还在加载模型")
    print()
    print("vLLM 的 /health 端点：")
    print("  - 模型加载中：返回 503 Service Unavailable")
    print("  - 模型就绪后：返回 200 OK")
    print()
    print("HEALTHCHECK 参数调优：")
    print()
    print("  --interval=30s")
    print("    每 30 秒检查一次，够用（LLM 状态不会秒级变化）")
    print()
    print("  --timeout=10s")
    print("    单次检查超时 10 秒")
    print("    vLLM 正常时 /health 响应 <100ms，10s 足够")
    print()
    print("  --start-period=120s")
    print("    ⚠️  关键参数！模型加载期间的检查不计入失败次数")
    print("    7B 模型加载约 30-60s，72B 可能 2-3 分钟")
    print("    设 120s 留有余量")
    print()
    print("  --retries=3")
    print("    连续 3 次失败才标记为 unhealthy")
    print("    避免偶发的网络抖动导致误判")
    print()
    print("在 docker-compose 中使用：")
    print("  depends_on:")
    print("    vllm:")
    print("      condition: service_healthy")
    print("  → Nginx/Prometheus 等 vLLm 就绪后才启动")
    print()


def explain_build_cache():
    """
    Docker 层缓存与构建优化
    """
    print("=" * 60)
    print("Docker 层缓存与构建优化")
    print("=" * 60)
    print()
    print("Docker 镜像由多层组成，每条指令创建一层：")
    print()
    print("  FROM vllm/vllm-openai:v0.19.0    → 层1: 基础镜像（~8GB）")
    print("  ENV HF_HOME=...                    → 层2: 环境变量（几字节）")
    print("  COPY requirements.txt .            → 层3: 依赖声明（几KB）")
    print("  RUN pip install -r requirements    → 层4: 安装依赖（可能很大）")
    print("  COPY . .                           → 层5: 源代码（几KB-MB）")
    print()
    print("缓存规则：")
    print("  - 某层变化 → 该层及后续所有层重新构建")
    print("  - 未变化的层 → 直接使用缓存，秒级完成")
    print()
    print("优化原则：变化少的指令放前面，变化多的放后面")
    print()
    print("  ❌ 差的顺序：")
    print("  COPY . .                    # 源码一变，后面的全重建")
    print("  RUN pip install -r req.txt  # 依赖没变也要重装！")
    print()
    print("  ✓ 好的顺序：")
    print("  COPY requirements.txt .     # 依赖声明单独复制")
    print("  RUN pip install -r req.txt  # 依赖不变就命中缓存")
    print("  COPY . .                    # 源码变化只重建最后一层")
    print()
    print("对 vLLM 部署的影响：")
    print("  - 直接用官方镜像 → 不需要构建，无缓存问题")
    print("  - 自定义镜像 → 注意层顺序，避免频繁重装依赖")
    print("  - 用 .dockerignore 排除不必要的文件（模型、__pycache__）")
    print()


