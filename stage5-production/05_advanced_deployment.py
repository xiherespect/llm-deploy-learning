def explain_rolling_update():
    """
    滚动更新与零停机
    """
    print("=" * 60)
    print("滚动更新与零停机部署")
    print("=" * 60)
    print()
    print("场景：vLLM 版本升级（如 v0.19.0 → v0.20.0）")
    print()
    print("❌ 直接更新（有停机时间）：")
    print("  docker compose down     # 所有服务停止")
    print("  docker compose up -d    # 所有服务重启")
    print("  → 停机时间 = 模型加载时间（30-90s）")
    print()
    print("✓ 滚动更新（零停机）：")
    print()
    print("  前提：至少 2 个 vLLM 实例 + Nginx 负载均衡")
    print()
    print("  步骤：")
    print("  1. 更新 vllm-1 镜像")
    print("     docker compose up -d --no-deps vllm-1")
    print("     → vllm-1 停止 → 加载模型 → 健康检查通过")
    print("     → 期间 vllm-2 继续服务（Nginx 只转发到 vllm-2）")
    print()
    print("  2. 等待 vllm-1 healthy")
    print("     docker compose ps  # 确认 vllm-1 状态为 healthy")
    print()
    print("  3. 更新 vllm-2 镜像")
    print("     docker compose up -d --no-deps vllm-2")
    print("     → vllm-2 停止 → 加载模型 → 健康检查通过")
    print("     → 期间 vllm-1 已在服务")
    print()
    print("  4. 两个实例都已更新，零停机完成")
    print()
    print("  关键：Nginx 的 health_check 或 passive check")
    print("  当某个后端不可用时，Nginx 自动排除，不转发请求")
    print()
    print("docker-compose 中的滚动更新配置：")
    print()
    print("  deploy:")
    print("    update_config:")
    print("      parallelism: 1        # 每次更新 1 个实例")
    print("      delay: 120s           # 更新间隔（等模型加载）")
    print("      order: start-first    # 先启新的再停旧的")
    print("    healthcheck:")
    print("      ...")
    print()
    print("注意：GPU 部署的滚动更新受 GPU 数量限制")
    print("  - 4 张 GPU，2 实例各用 2 张 → 无法同时运行 3 个实例")
    print("  - 滚动更新时需要先停旧实例，再启新实例")
    print("  - 停机时间 = 模型加载时间（不可避免的短暂中断）")
    print()


def explain_security_hardening():
    """
    生产环境安全加固
    """
    print("=" * 60)
    print("生产环境安全加固")
    print("=" * 60)
    print()
    print("1. 容器安全")
    print()
    print("   # 使用非 root 用户运行")
    print("   USER nobody")
    print()
    print("   # 只读文件系统（只保留可写目录）")
    print("   read_only: true")
    print("   tmpfs:")
    print("     - /tmp")
    print("     - /root/.cache")
    print()
    print("   # 禁止特权升级")
    print("   security_opt:")
    print("     - no-new-privileges:true")
    print()
    print("2. 网络安全")
    print()
    print("   # vLLM 不直接暴露到宿主机")
    print("   # 只通过 Nginx 反向代理访问")
    print("   vllm:")
    print("     ports: []               # 不映射端口到宿主机")
    print("     networks:")
    print("       - llm-deploy-net      # 只在内网可达")
    print()
    print("   # Nginx 是唯一对外暴露的服务")
    print("   nginx:")
    print("     ports:")
    print("       - '80:80'")
    print()
    print("   # 限制 /metrics 端点的访问")
    print("   location /metrics {")
    print("       allow 172.16.0.0/12;  # 只允许内网")
    print("       deny all;")
    print("       proxy_pass http://vllm_backend;")
    print("   }")
    print()
    print("3. 认证与密钥")
    print()
    print("   # vLLM 内置 API Key 认证")
    print("   vllm:")
    print("     command:")
    print("       - --api-key=your-secret-key")
    print()
    print("   # 客户端请求时带上 key")
    print("   curl -H 'Authorization: Bearer your-secret-key' ...")
    print()
    print("   # ⚠️ 不要在 docker-compose.yml 中硬编码密钥")
    print("   # 使用环境变量或 Docker secrets：")
    print("   vllm:")
    print("     environment:")
    print("       - VLLM_API_KEY=${VLLM_API_KEY}  # 从 .env 读取")
    print()
    print("4. HTTPS")
    print()
    print("   # Nginx SSL 终止")
    print("   server {")
    print("       listen 443 ssl;")
    print("       ssl_certificate /etc/nginx/ssl/cert.pem;")
