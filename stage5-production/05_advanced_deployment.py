def demo_model_warmup():
    """
    模型预热与冷启动优化
    """
    print("=" * 60)
    print("模型预热与冷启动优化")
    print("=" * 60)
    print()
    print("冷启动问题：")
    print()
    print("  vLLM 启动后的首次请求特别慢：")
    print()
    print("  [容器启动] → [加载模型权重 30-60s] → [首次请求]")
    print("                                            ↓")
    print("                                    [编译 CUDA Graph 10-30s]")
    print("                                    [分配 KV Cache 内存]")
    print("                                    [首次推理完成]")
    print("                                            ↓")
    print("                                    [后续请求正常速度]")
    print()
    print("  首次请求延迟 = 正常延迟 + CUDA Graph 编译时间")
    print("  用户感知：第一个问题等很久，后面就正常了")
    print()
    print("解决方案：模型预热（Warmup）")
    print()
    print("  在 HEALTHCHECK 变 healthy 后，自动发送一个预热请求：")
    print()
    print("  ```python")
    print("  import requests")
    print()
    print("  def warmup_vllm(url='http://localhost:8000/v1/chat/completions', model='Qwen/Qwen2.5-7B-Instruct'):")
    print("      \"\"\"发送预热请求，触发 CUDA Graph 编译\"\"\"")
    print("      print('开始模型预热...')")
    print("      payload = {")
    print("          'model': model,")
    print("          'messages': [{'role': 'user', 'content': 'hi'}],")
    print("          'max_tokens': 1,")
    print("          'temperature': 0,")
    print("      }")
    print()
    print("      start = time.time()")
    print("      resp = requests.post(url, json=payload)")
    print("      warmup_time = time.time() - start")
    print()
    print("      if resp.status_code == 200:")
    print("          print(f'预热完成！耗时: {warmup_time:.2f}s')")
    print("      else:")
    print("          print(f'预热失败: {resp.status_code}')")
    print("  ```")
    print()
    print("  更优雅的方式：在 docker-compose 中用启动脚本：")
    print()
    print("  entrypoint: /app/startup.sh")
    print()
    print("  # startup.sh")
    print("  #!/bin/bash")
    print("  python -m vllm.entrypoints.openai.api_server \"$@\" &")
    print("  VLLM_PID=$!")
    print()
    print("  # 等待服务就绪")
    print("  until curl -sf http://localhost:8000/health; do sleep 2; done")
    print()
    print("  # 发送预热请求")
    print("  curl -s http://localhost:8000/v1/chat/completions \\")
    print("    -H 'Content-Type: application/json' \\")
    print("    -d '{\"model\":\"Qwen/Qwen2.5-7B-Instruct\",\"messages\":[{\"role\":\"user\",\"content\":\"hi\"}],\"max_tokens\":1}'")
    print()
    print("  wait $VLLM_PID")
    print()
    print("  这样容器 healthy 后，CUDA Graph 已编译，首次用户请求不会卡")
    print()


def demo_rate_limiting_strategy():
    """
    三层限流策略
    """
    print("=" * 60)
    print("三层限流策略")
    print("=" * 60)
    print()
    print("LLM 服务的限流比普通 API 更复杂，因为：")
    print("  - 请求耗时长（秒级 vs 毫秒级）")
    print("  - GPU 显存是硬约束，不是 CPU 那样能超售")
    print("  - 并发过高 → KV Cache 溢出 → 抢占 → 所有用户都慢")
    print()
    print("┌─────────────────────────────────────────────────────┐")
    print("│  第一层：Nginx 限流（请求级）                         │")
    print("├─────────────────────────────────────────────────────┤")
    print("│  limit_req_zone $binary_remote_addr zone=api:10m   │")
    print("│      rate=10r/s;                                     │")
    print("│  limit_req zone=api burst=20 nodelay;               │")
    print("│                                                      │")
    print("│  控制：每 IP 每秒请求数                               │")
    print("│  效果：防止单个客户端发送过多请求                      │")
    print("│  限制：不知道每个请求的 token 数                      │")
    print("└─────────────────────────────────────────────────────┘")
    print()
    print("┌─────────────────────────────────────────────────────┐")
    print("│  第二层：vLLM 并发限制（GPU 级）                      │")
    print("├─────────────────────────────────────────────────────┤")
    print("│  --max-num-seqs 64                                   │")
    print("│  --gpu-memory-utilization 0.9                        │")
    print("│                                                      │")
    print("│  控制：同时处理的请求序列数                           │")
    print("│  效果：超过限制的请求排队等待                         │")
    print("│  限制：只控制并发数，不区分用户                       │")
    print("└─────────────────────────────────────────────────────┘")
    print()
    print("┌─────────────────────────────────────────────────────┐")
    print("│  第三层：应用层配额（用户级）                         │")
    print("├─────────────────────────────────────────────────────┤")
    print("│  自定义 API 网关或中间件：                            │")
    print("│  - 按 API Key 限流：每用户每分钟 N 个请求             │")
    print("│  - 按 token 用量限流：每用户每天 M 个 token           │")
    print("│  - 优先级队列：付费用户优先，免费用户排队             │")
